package slidePuzzle;

public class InternalTile {

	private Tile NE;
	private Tile NW;
	private Tile SE;
	private Tile SW;
	
	public InternalTile(Tile NE, Tile NW, Tile SE, Tile SW) {
		this.NE = NE;
		this.NW = NW;
		this.SE = SE;
		this.SW = SW;
	}
}

/*SIZE GETTER*/
public int size() {
	return this.size();
}
